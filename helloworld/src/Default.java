//default values
public class Default {
    byte b;
    short s;
    int x;
    long x1;
    float f;
    double d;
    char ch;
    boolean b1;
    public static void main(String[] args)
    {
        Default d=new Default();
        System.out.println("byte b= "+d.b);
        System.out.println("short s= "+d.s);
        System.out.println("int x= "+d.x);
        System.out.println("long x1= "+d.x1);
        System.out.println("float f= "+d.f);
        System.out.println("double d= "+d.d);
        System.out.println("char ch= "+d.ch);
        System.out.println("boolean b1= "+d.b1);
    }
}